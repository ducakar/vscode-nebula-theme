/**
 * Defines the fontStyle attribute values that can be applied to a color token.
 */
export enum FontStyle {
	Italics = 'italic',
	Bold = 'bold',
	Normal = 'normal',
	Underline = 'underline',
	BoldUnderline = 'bold underline',
	BoldItalics = 'bold italic',
	ItalicsUnderline = 'italic underline',
	None = ''
}